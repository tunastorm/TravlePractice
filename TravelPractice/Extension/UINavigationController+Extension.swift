import UIKit


extension UINavigationController {
    func setLayoutForRestuarant() {
        let navigationBar = self.navigationBar
        navigationBar.backgroundColor = .white
        navigationBar.barTintColor = .white
        navigationBar.tintColor = .black
    }
}
